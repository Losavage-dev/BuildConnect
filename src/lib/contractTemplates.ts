export function buildContractDocument(
  id: ContractTemplateId,
  fields: { city: string; docDate: string },
): string {
  const city = fields.city.trim() || "[ГОРОД]";
  const docDate = fields.docDate.trim() || "[ДАТА_ДОГОВОРА]";
  let body: string;
  switch (id) {
    case "services":
      body = bodyServices();
      break;
    case "supply":
      body = bodySupply();
      break;
    case "act":
      body = bodyAct();
      break;
    default:
      body = bodyServices();
  }
  return body.replaceAll("[ГОРОД]", city).replaceAll("[ДАТА_ДОГОВОРА]", docDate);
}
